function compareForTime(Route) {
  for (let i = 0; i < Route.length; i++) {
    for (let j = 0; j < Route.length; j++) {
      if (i == j) continue;
      if (
        Route[i].time === Route[j].time &&
        Route[i].address === Route[j].address
      ) {
        Route.splice(j, 1);
      }
    }
  }
  console.log(Route.length);
}

